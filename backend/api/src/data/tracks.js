export function findTrackMetadata(trackValue) {
  if (!trackValue) {
    return null;
  }

  const normalizedTrack = trackValue.toLowerCase();
  return (
    trackMetadata.find((track) =>
      track.aliases.some((alias) => alias.toLowerCase() === normalizedTrack)
    ) ?? null
  );
}

export function findTrackMetadataFromSession(session) {
  if (!session) {
    return null;
  }

  const candidateValues = [
    session.country_name,
    session.circuit_short_name,
    session.location,
    session.session_name
  ]
    .filter(Boolean)
    .map((value) => String(value).toLowerCase());

  return (
    trackMetadata.find((track) =>
      track.aliases.some((alias) =>
        candidateValues.some((candidate) => candidate.includes(alias.toLowerCase()))
      )
    ) ?? null
  );
}

function createFallbackTrackOption(session) {
  const name = session.circuit_short_name ?? session.location ?? session.country_name;
  if (!name) {
    return null;
  }

  const key = String(name)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");

  return {
    key,
    name,
    countryName: session.country_name ?? null,
    circuitShortName: session.circuit_short_name ?? null,
    location: session.location ?? null,
    aliases: [name],
    layoutUrl: null,
    layoutAlt: null
  };
}

export function buildTrackOptionsFromSessions(sessions) {
  const dedupedTracks = new Map();

  for (const session of sessions) {
    const metadata = findTrackMetadataFromSession(session) ?? createFallbackTrackOption(session);
    if (!metadata || dedupedTracks.has(metadata.key)) {
      continue;
    }

    dedupedTracks.set(metadata.key, {
      ...metadata,
      seasonOrderDate: session.date_start ?? null
    });
  }

  return Array.from(dedupedTracks.values())
    .sort((left, right) => {
      const leftDate = left.seasonOrderDate ? String(left.seasonOrderDate) : "";
      const rightDate = right.seasonOrderDate ? String(right.seasonOrderDate) : "";

      if (leftDate && rightDate && leftDate !== rightDate) {
        return leftDate.localeCompare(rightDate);
      }

      if (leftDate && !rightDate) {
        return -1;
      }

      if (!leftDate && rightDate) {
        return 1;
      }

      return left.name.localeCompare(right.name);
    })
    .map(({ seasonOrderDate, ...track }) => track);
}
